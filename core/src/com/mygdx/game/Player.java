package com.mygdx.game;

public class Player {
	private int _wins;
	private int _losses;
	
	public Player() {
		
	}

	public int get_wins() {
		return _wins;
	}

	public void set_wins(int _wins) {
		this._wins = _wins;
	}

	public int get_losses() {
		return _losses;
	}

	public void set_losses(int _losses) {
		this._losses = _losses;
	}
	
	
	
}

